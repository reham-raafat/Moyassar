import type { Place, PlaceFeatures, PlaceCategory, OpeningHours } from "@/types";

const STD_HOURS: OpeningHours = [0, 1, 2, 3, 4, 5, 6].map((d) => ({
  day: d,
  open: "09:00",
  close: "22:00",
}));

function scoresFor(f: PlaceFeatures) {
  const mRaw =
    (f.ramps ? 10 : 0) +
    (f.elevator ? 10 : 0) +
    (f.accessible_restroom ? 8 : 0) +
    (f.accessible_parking ? 6 : 0) +
    (f.accessible_entrance ? 4 : 0) +
    (f.wide_doorways ? 2 : 0);
  const vRaw =
    (f.braille_signage ? 10 : 0) +
    (f.tactile_paving ? 8 : 0) +
    (f.audio_assistance ? 7 : 0) +
    (f.low_counter ? 5 : 0);
  const hRaw =
    (f.visual_alerts ? 10 : 0) + (f.audio_assistance ? 10 : 0) + (f.accessible_seating ? 10 : 0);
  const m = Math.min(100, Math.round(mRaw * 2.5));
  const v = Math.min(100, Math.round(vRaw * (100 / 30)));
  const h = Math.min(100, Math.round(hRaw * (100 / 30)));
  return { m, v, h, o: Math.round(m * 0.4 + v * 0.3 + h * 0.3) };
}

// Build a place quickly from compact input.
type Seed = {
  id: string;
  name_ar: string;
  name_en: string;
  category: PlaceCategory;
  governorate: string;
  city: string;
  district: string;
  address_ar: string;
  address_en: string;
  lat: number;
  lng: number;
  phone?: string;
  website?: string;
  image: string;
  feats: Partial<PlaceFeatures>;
  verified?: boolean;
  verifier?: string;
  verifyAt?: string;
  verifyNote?: string;
};

function mk(s: Seed): Place {
  const features: PlaceFeatures = {
    ramps: false,
    elevator: false,
    accessible_restroom: false,
    accessible_parking: false,
    accessible_entrance: false,
    wide_doorways: false,
    braille_signage: false,
    tactile_paving: false,
    audio_assistance: false,
    visual_alerts: false,
    low_counter: false,
    accessible_seating: false,
    ...s.feats,
  };
  const sc = scoresFor(features);
  return {
    id: s.id,
    name_ar: s.name_ar,
    name_en: s.name_en,
    category: s.category,
    governorate: s.governorate,
    city: s.city,
    district: s.district,
    address_ar: s.address_ar,
    address_en: s.address_en,
    lat: s.lat,
    lng: s.lng,
    phone: s.phone,
    website: s.website,
    images: [s.image],
    photo_url: s.image,
    opening_hours: STD_HOURS,
    features,
    score_overall: sc.o,
    score_mobility: sc.m,
    score_visual: sc.v,
    score_hearing: sc.h,
    verified_by_admin: Boolean(s.verified),
    verification: s.verified
      ? {
          verified_at: s.verifyAt ?? "2025-08-12T10:00:00Z",
          verified_by: s.verifier ?? "Ministry of Social Solidarity",
          notes: s.verifyNote ?? "On-site audit completed — accessibility features confirmed.",
          previous_status: "community",
        }
      : undefined,
    created_at: "2025-01-10T08:00:00Z",
  };
}

