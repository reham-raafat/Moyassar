type Strings = (typeof STRINGS)["ar"];
type Ctx = { locale: Locale; dir: "rtl" | "ltr"; t: Strings; setLocale: (l: Locale) => void };
const I18nCtx = createContext<Ctx | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    if (typeof window === "undefined") return "ar";
    return (localStorage.getItem("moyassar.locale") as Locale | null) ?? "ar";
  });

  useEffect(() => {
    if (typeof document === "undefined") return;
    document.documentElement.lang = locale;
    document.documentElement.dir = locale === "ar" ? "rtl" : "ltr";
    localStorage.setItem("moyassar.locale", locale);
  }, [locale]);

  const value: Ctx = {
    locale,
    dir: locale === "ar" ? "rtl" : "ltr",
    t: STRINGS[locale] as Strings,
    setLocale: setLocaleState,
  };
  return <I18nCtx.Provider value={value}>{children}</I18nCtx.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nCtx);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}

